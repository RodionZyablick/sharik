import random

def гра_вгадай_число():
  """
  Запускає гру "Вгадай число".

  Програма загадує випадкове ціле число в заданому діапазоні,
  а гравець намагається його вгадати. Програма надає підказки
  "більше" або "менше" після кожної спроби. Гра завершується,
  коли гравець вгадує число, і програма виводить кількість
  використаних спроб.
  """
  нижня_межа = 1
  верхня_межа = 100
  загадане_число = random.randint(нижня_межа, верхня_межа)
  кількість_спроб = 0

  print(f"Я загадав число від {нижня_межа} до {верхня_межа}. Спробуй вгадати!")

  while True:
    спроба = отримати_припущення_гравця()
    кількість_спроб += 1

    if спроба < загадане_число:
      print("Загадане число більше.")
    elif спроба > загадане_число:
      print("Загадане число менше.")
    else:
      print(f"Вітаю! Ти вгадав число {загадане_число} за {кількість_спроб} спроб!")
      break

def отримати_припущення_гравця():
  """
  Отримує припущення гравця від користувача та перевіряє,
  чи є введене значення цілим числом.

  Повертає:
      int: Ціле число, введене гравцем.
      У випадку некоректного введення (не ціле число),
      просить ввести знову.
  """
  while True:
    try:
      припущення = int(input("Введи своє припущення: "))
      return припущення
    except ValueError:
      print("Будь ласка, введи ціле число.")

# Основна частина програми, яка запускає гру
if __name__ == "__main__":
  гра_вгадай_число()